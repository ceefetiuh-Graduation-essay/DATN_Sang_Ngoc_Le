/**
  * @brief  Rx Transfer completed callbacks. - Chuong trinh phuc vu ngat nhan du lieu UART
  * @param  huart: Pointer to a UART_HandleTypeDef structure that contains
  *                the configuration information for the specified UART module.
  * @retval None
  */
void HAL_UART_RxCpltCallback(UART_HandleTypeDef *huart)
{
	if(huart->Instance == huart3.Instance)		// If data is received via UART3
	{			
			
		if(DBG_RX_byte[0] == 'R')			//if received data = [Tab]
		{
			printf("\r\n\r\nP.Log:> User Reset!\r\n");  // Reset the MCU
			//HAL_Delay(500);	
			NVIC_SystemReset();			
		}
		
		if (DBG_RX_index == 0)
		{
			for (uint8_t i = 0;i < 100;i++) DBG_RX_Buffer[i] = '\0'; //clear Rx_Buffer before receiving new data	
		}	
		
		if(DBG_RX_byte[0] != '\n')	//if received data different from ascii(\n)
		{
			DBG_RX_Buffer[DBG_RX_index++] = DBG_RX_byte[0];	//add data to Rx_Buffer
			putchar(DBG_RX_byte[0]); //Display character 
		}
			
		else if(DBG_RX_byte[0] == '\n')			//if received data = \n [Enter]
		{
			//DBG_RX_Buffer[DBG_RX_index++] = DBG_RX_byte[0];
			DBG_RX_index = 0;
			DBG_Transfer_cplt = 1;	//transfer complete, data is ready to read
		}	
		//HAL_UART_Receive_IT(&huart1,DBG_RX_byte,1); // activate UART receive interrupt every time - NOT USE IN DMA Mode
	}
	
	if(huart->Instance == huart2.Instance)		// Neu co du lieu truyen den uart2
	{
			
	}
	
	if(huart->Instance == huart1.Instance)		// Neu co du lieu truyen den uart1
	{
		//HAL_GPIO_TogglePin(LD3_G_GPIO_Port, LD3_G_Pin);
		
		SIM_Buff[gsm_buff_count++] = GSM_RX_byte[0];
		
		if(GSM_RX_indx == 0) 
		{
			for (uint8_t i = 0;i < 100;i++)
			{
					GSM_RX_Buffer[i] = '\0'; //clear Rx_Buffer before receiving new data	
			}
		}	
		
		if(gsm_buff_count == 0) 
		{
			for (uint16_t i = 0;i < 1024;i++)
			{
				SIM_Buff[i] = '\0'; //clear Rx_Buffer before receiving new data	
			}
		}	
	}
}
